package org.eoem.lang;

public class HowToSwitch {
    public static void main(String[] args) {
        int a = 3;
        switch (a) {
            case 1:
                System.out.println("### 1");
            case 2:
                System.out.println("### 2");
            case 3:
                ;
            case 4:
                System.out.println("### 4");
        }
        
    }
}
